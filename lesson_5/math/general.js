// Absolute values:
let degrees = -180;
console.log(Math.abs(degrees)); // 180

// Square root:
console.log(Math.sqrt(16777216)); // 4096

// Powers:
console.log(Math.pow(16, 6)); // 16 to the power of 6

// Rounding:
let a = 50.72;
let b = 49.2;
let c = 49.86;

console.log(Math.floor(a)); // 50
console.log(Math.ceil(b)); // 50
console.log(Math.round(c)); // 50